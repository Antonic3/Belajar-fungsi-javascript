function bagi(a,b){
    hasilBagi = a / b;
    return hasilBagi;
}

// memanggil fungsi
console.log("pembagian dari 10/2 = ",bagi(10, 2)); // maka akan menghasilkan -> 5
console.log("pembagian dari 20/2 = ",bagi(20, 2)); // maka akan menghasilkan -> 10
console.log("pembagian dari 30/2 = ",bagi(30, 2)); // maka akan menghasilkan -> 15

// perkalian
function kali(a,b){
    hasilBagi = a * b;
    return hasilBagi;
}

// memanggil fungsi
console.log("perkalian dari 10*2 = ",kali(10, 2)); // maka akan menghasilkan -> 20
console.log("perkalian dari 20*2 = ",kali(20, 2)); // maka akan menghasilkan -> 40
